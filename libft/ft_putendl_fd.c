#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	char	c;

	if (!s)
		return ;
	while (*s)
	{
		c = *s;
		write(fd, &c, 1);
		s++;
	}
	write(fd, "\n", 1);
}
