#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	char			*st;

	st = s;
	i = 0;
	if (n == 0)
		return ;
	while (i < n)
	{
		st[i] = '\0';
		i++;
	}
}
